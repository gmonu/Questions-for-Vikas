import java.util.*;
import java.lang.Math;

class MathClass
{
	public static void main(String[] args) {
		int a = Math.min(3,7);
		int b = Math.min(10, -5);
		System.out.println(Math.min(a,b));
		System.out.println(Math.max(a,b));
		System.out.println(Math.round(1.524));
		System.out.println(Math.abs(65));	
	}
}